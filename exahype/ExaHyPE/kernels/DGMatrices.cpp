void kernels::freeDGMatrices(const std::set<int>& orders) {
  const int MAX_ORDER = 9;

  // vectors
  for (int ii = 0; ii < MAX_ORDER + 1; ii++) {
    for (int jj = 0; jj < ii + 1; jj++) {
      delete[] Kxi[ii][jj];
      delete[] iK1[ii][jj];
      delete[] dudx[ii][jj];
    }

    delete[] F0[ii];
    delete[] FLCoeff[ii];
    delete[] FRCoeff[ii];

    // matrices
    delete[] FCoeff[ii][0];
    delete[] FCoeff[ii][1];
    delete[] FCoeff[ii];

    delete[] Kxi[ii];
    delete[] iK1[ii];
    delete[] dudx[ii];
  }

  delete[] F0;
  delete[] FLCoeff;
  delete[] FRCoeff;

  delete[] FCoeff;
  delete[] Kxi;
  delete[] iK1;
  delete[] dudx;

  for (int ii = 0; ii < MAX_ORDER + 1; ii++) {
    for (int jj = 0; jj < ii + 1; jj++) {
      delete[] equidistantGridProjector1d[ii][jj];
      delete[] fineGridProjector1d[ii][0][jj];
      delete[] fineGridProjector1d[ii][1][jj];
      delete[] fineGridProjector1d[ii][2][jj];
    }
    delete[] fineGridProjector1d[ii][0];
    delete[] fineGridProjector1d[ii][1];
    delete[] fineGridProjector1d[ii][2];
    delete[] fineGridProjector1d[ii];

    delete[] equidistantGridProjector1d[ii];
  }
  delete[] equidistantGridProjector1d;
  delete[] fineGridProjector1d;
}

